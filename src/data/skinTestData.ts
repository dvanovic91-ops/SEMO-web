/** 20개 답변 점수 배열로 피부 타입 계산 (봇 calc_type과 동일) */
export function calcSkinType(answers: number[]): { type: string; scores: Record<1 | 2 | 3 | 4, number> } {
  const s1 = answers.slice(0, 5).reduce((a, b) => a + b, 0);
  const s2 = answers.slice(5, 10).reduce((a, b) => a + b, 0);
  const s3 = answers.slice(10, 15).reduce((a, b) => a + b, 0);
  const s4 = answers.slice(15, 20).reduce((a, b) => a + b, 0);
  const scores: Record<1 | 2 | 3 | 4, number> = { 1: s1, 2: s2, 3: s3, 4: s4 };
  const type =
    (s1 >= 0 ? 'D' : 'O') +
    (s2 >= 0 ? 'S' : 'R') +
    (s3 >= 0 ? 'P' : 'N') +
    (s4 >= 0 ? 'W' : 'T');
  return { type, scores };
}

/**
 * DB에 저장된 타입 코드만 있을 때(프로필 «прошлый тест») «Баллы» 줄 표시용.
 * 실제 20문항 합계와 다를 수 있으나 D/O·S/R·P/N·W/T 방향과 맞춤.
 */
export function approximateScoresFromSkinTypeCode(type: string): Record<1 | 2 | 3 | 4, number> {
  const code = type.trim().toUpperCase();
  if (code.length !== 4) return { 1: 0, 2: 0, 3: 0, 4: 0 };
  return {
    1: code[0] === 'D' ? 2 : -2,
    2: code[1] === 'S' ? 2 : -2,
    3: code[2] === 'P' ? 2 : -2,
    4: code[3] === 'W' ? 2 : -2,
  };
}

export function getPartTitle(questionIndex: number): string {
  if (questionIndex < 5) return PART_TITLES[0];
  if (questionIndex < 10) return PART_TITLES[1];
  if (questionIndex < 15) return PART_TITLES[2];
  return PART_TITLES[3];
}
